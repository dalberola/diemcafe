<h1>Mehrsprachigen Testlauf der DiEM Café</h1>

<p>Liebe DiEMers,</p>

<p>Herzlich willkommen zum ersten mehrsprachigen Testlauf der DiEM Café Plattform. Wir freuen uns sehr euch hier begrüßen zu dürfen und hoffen, euch auch als Gastgeber gewonnen zu haben. Wenn sich nicht genügend Teilnehmer als Gastgeber anmelden, werden die Café-Tische mit mehr Gästen, als angenehm ist, besetzt sein und das wäre schade.</p>

<p>Auch wenn es sich hier um einen Testlauf handelt, würden wir gerne die Chance nutzen und neben der Plattform auch die Inhalte, die im ersten DiEM-weiten Einsatz des DiEM Cafés besprochen werden sollen, thematisieren.</p>

<p>Die World Café Methode funktioniert nach dem Prinzip, dass dieselbe Frage mit verschiedenen Personen mehrere Male in Kleingruppen diskutiert wird. Am Samstag, den 2. September, werden wir Fragen zur Politisierung von DiEM25 stellen. Dazu sind drei Abschnitte ("Sektionen") geplant, innerhalb derer jeweils eine Frage diskutiert werden wird - simultan &amp; in vielen Sprachen gleichzeitig.</p>

<p>Währen des heutigen Events (26. August) wollen wir zwei Fragen zur Diskussion stellen:</p>

<ol><li>- Welche Fragen wollen wir während des DiEM Cafés am 2. September besprechen?</li>
<li>- Wie findet ihr das DiEM Café bisher & was würdet ihr verbessern?</li></ol>

<p>Bevor wir anfangen noch eine Bitte: Diese Plattform existiert nur, weil sehr wenig Menschen in sehr kurzer Zeit sehr großen Aufwand betrieben haben - es könnte also durchaus sein, dass etwas schiefläuft. Wir bitten euch um Geduld und Verständnis und sind um jede Rückmeldung mit Fehlerbeschreibung und konstruktiven Kommentaren dankbar: did.diem25@gmail.com</p>

<p>Vielen Dank nochmals für eure Teilnahme</p>
<p>Carpe DiEM</p>
<p>Die DiEM Café Arbeitsgruppe</p>
