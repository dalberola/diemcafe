  <h2>Notas / Reflexiones</h2>
<p>Esta discusi&oacute;n debe producir un resultado tangible para que tengamos una mejor comprensi&oacute;n de nuestros puntos de vista comunes o divergentes. Con el fin de hacer la informaci&oacute;n manejable las notas deben tener una longitud m&aacute;xima de 140 caracteres, y un pad estar&aacute; lleno una vez que 5 notas se anotan.
</p>
<p>Las notas no se pueden eliminar, y deben reflejar los temas m&aacute;s importantes, las ideas o las conclusiones discutidas, por lo que te orgamos reflexiones un minuto antes de apretar el bot&oacute;n de &ldquo;Compartir&rdquo;. Te invitamos a discutir las notas en el chat Zoom antes de compartirlas en la pesta√±a de la mesa. Una vez transcurrido el tiempo, la ventana de texto se bloquear&aacute; bloqueada en unos minutos para pasar a la siguiente mesa.</p>
