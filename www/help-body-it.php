  <div class="jumbotron jumbotron-alt">
    <h2>Come gestire un tavolo</h2>

    <p>Abbiamo bisogno di un po' di aiuto da parte tua. DiEM Café è
    una piattaforma che, per funzionare, dipende anche dagli utenti
    che devono svolgere alcune mansioni. Molto semplici, certo, ma
    essenziali. Ogni tavolo del Café consiste in una scheda del
    browser nel sito di DiEM Café e di un video di Zoom nella
    relativa finestra per la videoconferenza. In ogni tavolo uno
    degli utenti deve essere il "padrone di casa" che è
    responsabile della creazione della conferenza e di pubblicare
    l'identificativo del tavolo nella relativa scheda. Inoltre ti
    chiediamo, se stai gestendo tu un tavolo, di completare gli
    appunti durante gli ultimi minuti della conferenza, se non
    l'hanno fatto gli altri utenti.</p>

    <p>Chi gestisce un tavolo non è necessariamente un moderatore,
    però deve svolgere alcune semplici attività che sono
    dettagliate nella sezione del <a href=
    "#etiquette">protocollo</a>.</p><a href="#top">Torna su</a>
  </div><a name="etiquette" id="etiquette"></a>

  <div class="jumbotron">
    <h2>Il protocollo</h2>

    <p>Principalmente DiEM Café permette di condividere apertamente
    con gli altri membri di DiEM le nostre posizioni. Dobbiamo
    essere capaci di creare un'atmosfera coperativa, costruttiva e
    rilassante. Per questo proponiamo un semplice <a href=
    "#conduct">codice di comportamento</a>.</p>

    <p>Per utilizzare il tempo nel modo più efficace,
    suggeriamo:</p>

    <ol>
      <li>La discussione inizia appena tutti i partecipanti sono
      presenti</li>

      <li>Il padrone di casa chiede agli altri partecipanti se
      preferiscono una sessione sponanea o moderata</li>

      <li>Se necessario si sceglie un moderatore</li>

      <li>Breve introduzione di tutti i partecipanti al tavolo</li>

      <li>Il padrone di casa riassume brevemente i risultati della
      sessione precedente (massimo due minuti)</li>

      <li>Si apre il dibattito</li>

      <li>Si prendono gli appunti come indicato</li>

      <li>Partecipa attivamente e torna per il prossimo evento</li>
    </ol><a href="#top">Torna su</a>
  </div><a name="conduct" id="conduct"></a>

