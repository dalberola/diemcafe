  <div class="jumbotron">
    <h2>I tavoli</h2>

    <p>Un tavolo del Café è un luogo virtuale dove la stessa
    domanda è affrontata da diversi gruppi di persone durante più
    turni. Solo chi ospita rimane allo stesso tavolo, dove si parla
    la stessa lingua per tutti i turni di una sezione.</p>

    <p>All'inizio dell'evento (o alla fine di ogni turno), nel tu
    browser appare un nuovo pulsante che ti permette di collegarti
    a un tavolo. Una volta collegato, quando chi ospita il tavolo è
    pronto, appare il link alla videoconferenza con Zoom. Clicca su
    "collegati" e se non dovesse funzionare, puoi sempre copiare e
    incollare il link a mano in una nuova scheda. Suggeriamo di non
    chiudere le finestre o schede durante un evento, tanto meno
    finchè il turno corrente non è concluso.</p>

    <p>Nella parte sinistra della schermata del tavolo trovi il
    riassunto degli appunti relative ai turni conclusi e, a destra,
    una spazio vuoto per aggiungere appunti. Per favore prima di
    iniziare leggi <a href="#notes">qui</a> come prendere appunti.
    Alla fine di ogni turno questo spazio con gli appunti conterrà
    le idee più importanti che sono emerse durante la
    discussione.</p><a href="#top">Torna su</a>
  </div><a name="notes" id="notes"></a>

  <div class="jumbotron jumbotron-alt">
    <h2>Gli appunti</h2>

    <p>Queste discussioni devono produrre risultati tangibili di
    modo che ogni partecipante possa comprendere i punti in comune
    e quelli di disaccordo con il resto del gruppo. Per gestire gli
    appunti al meglio, questi non dovranno superare i 140
    caratteri, e uno spazio per appunti sarà "pieno" quando ne
    contiene cinque.</p>

    <p>Gli appunti non possono essere cancellati e devono
    riguardare i temi principali, le idee discusse e le conclusioni
    a cui si è giunti, pertanto, prenditi un minuto prima di
    cliccare su "condividi". Ti invitiamo a discutere gli appunti
    presi con gli altri partecipanti al tavolo prima di cliccare su
    "condividi". Quando il tempo a disposizione termina, lo spazio
    per appunti si blocca - è il momento di cambiare
    tavolo.</p><a href="#top">Torna su</a>
