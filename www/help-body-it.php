    <p>Zoom è lo strumento di videoconferenza utilizzato da DiEM
    Café. Devi scaricarlo preventivamente e testarlo, possibilmente
    con un amico, per evitare sorprese con l'audio.</p>

    <p>Puoi scaricare il Cliente Zoom per Meeting direttamente
    <a href="https://zoom.us/download" target="_blank">qui</a> per
    la maggior parte dei sistemi operativi su PC, dispositivi
    mobili o il plugin per Chrome e Firefox. Per l'installazione
    sotto Linux segui <a href=
    "https://support.zoom.us/hc/en-us/articles/204206269-Linux-Installation"
