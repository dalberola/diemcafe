    <h2>Turni</h2>

    <p>Ogni turno di discussione dura 22 minuti, periodo durante il
    quale tutti i gruppi discutono, ognuno attorno al proprio
    tavolo, nella stessa conferenza di Zoom, la domanda che appare
    sullo schermo. Alla fine di ogni turno, un avviso acustico
    segnala lo scadere del tempo a disposizione. Dopo questo
    segnale ci sono ancora tre minuti per completare il
    turno.</p><a href="#top">Torna su</a>
