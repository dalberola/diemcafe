  <div class="jumbotron">
    <h2>La lingua</h2>

    <p>L'obiettivo è di aperti a quante più persone possibile.
    Molta gente parla solo la lingua madre e per questo la lingua
    ha la precedenza quando si assegnano le persone ai tavoli.
    Però, se ci sono poche persone che parlano solo una certa
    lingua, magari una non molto diffusa come l'Esperanto,
    potrebbero non incontrare persone di altre nazionalità o
    culture. Ci dispiace per questi casi ma è il meglio che
    possiamo fare.</p>

    <p>Se ti senti a tuo agio a parlare più di una lingua, non
    essere timido e dillo, specialmente se è una lingua poco
    diffusa. Le tue capacità linguistiche aiutano a creare una
    miscela interessante che converte DiEM Café in una vera
    esperienza paneuropea.</p><a href="#top">Torna su</a>
