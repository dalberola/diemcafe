    <p>Quando hai installato Zoom, cliccando su "Unisciti" dal link
    del tuo <a href="#table">tavolo</a> si apre Zoom e ti collega
    all'evento.</p>

    <p>Per ospitare un tavolo devi fare il login in Zoom e creare
