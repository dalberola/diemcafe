
<h1>DiEM Caf&eacute; Multilingual Trial Run</h1>

<p>Dear DiEMista,</p>

<p>Welcome to this first multilingual trial of the DiEM Café platform. We are very glad to have you here, and we hope you have volunteered to host a table because if there are not enough hosts, there will be too many people in each table.</p>

<p>Although this is a platform test, we would like to take the opportunity to discuss the contents of the first DiEM wide #diemcafe that will take place on Saturday sept 2nd.</p>

<p>The World Café methodology consist of discussing the same question with different people several times in small groups. On the first DiEM Café we will be discussing the question of the politicisation of DiEM25. It will consist of three sections, in each one of them one question will be discussed, simultaneously in many languages.</p>

<p>During today's event (26th Aug) we will have two questions to discuss:</p>

<ol><li>Which questions should we ask ourselves in the Diem Café?</li>
<li>What do you think about the #diemcafe and what to improve?</li></ol>
<p>Before we start, please understand that this platform is the effort of very few people in very short time, and things may go wrong. Please be patient and understanding and make sure that you let us know what went wrong by writing to <a href="mailto:did.diem25@gmail.com">did.diem25@gmail.com</a>.</p>

<p>Thank you once again for your participation</p>
<p>Carpe DiEM</p>
<p>The DiEM Café working group</p>
