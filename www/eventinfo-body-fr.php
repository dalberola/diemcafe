<h1>Essai multilingue du DiEM Café</h1>

  <p>Cher DiEMista,</p>

<p>Bienvenue dans ce premier essai multilingue de la plateforme DiEM Café. Nous sommes très heureux de vous avoir ici, et nous espérons que vous vous êtes porté volontaire pour héberger une table, car s'il n'y a pas assez d'hôtes, il y aura trop de personnes dans chaque table.</p>

<p>Bien qu'il s'agisse d'un test de plate-forme, nous aimerions profiter de l'occasion pour discuter du contenu du premier #diemcafe avec le reste du DiEM. le samedi 2 Septembre.</p>

<p>La méthodologie World Café consiste à discuter de la même question avec différentes personnes plusieurs fois en petits groupes. Dans notre premièr DiEM Café nous discuterons la politisation de DiEM25. Il se composera de trois sections, chacune d'entre elles sera discutée simultanément dans plusieurs langues.</p>

<p>Au cours de notre test multilangue (26 août), nous aurons deux questions à discuter:</p>
<p>Quelles questions devrions-nous nous demander dans le Diem Café?</p>
<p>Que pensez-vous du café et de ce qu'il faut améliorer?</p>

<p>Avant de commencer, veuillez comprendre que cette plate-forme est l'effort de très peu de personnes en très peu de temps, et certains aspects peuvend ne pas fonctionner. Soyez patient et compréhensif et assurez-vous de nous informer de ce qui a mal tourné en écrivant à did.diem25@gmail.com.</p>

<p>Merci encore une fois pour votre participation</p>
<p>Carpe Diem</p>
<p>Le groupe de travail DiEM Café</p>
